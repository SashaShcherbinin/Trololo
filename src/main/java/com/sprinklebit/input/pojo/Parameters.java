package com.sprinklebit.input.pojo;

public class Parameters {

    private int rows;

    private int columns;

    private int vehicles;

    private int bonuses;

    private int rides;

    private int steps;

    public Parameters(int[] parameters) {
    }

    public int getRows() {
        return rows;
    }

    public int getColumns() {
        return columns;
    }

    public int getVehicles() {
        return vehicles;
    }

    public int getBonuses() {
        return bonuses;
    }

    public int getRides() {
        return rides;
    }

    public int getSteps() {
        return steps;
    }
}
